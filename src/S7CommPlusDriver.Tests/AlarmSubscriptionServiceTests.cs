using System.IO;
using System.Linq;
using S7CommPlusDriver.Internal;
using Xunit;

namespace S7CommPlusDriver.Tests
{
    /// <summary>
    /// Verifies the protocol object graph used to create live PLC alarm subscriptions.
    /// </summary>
    public sealed class AlarmSubscriptionServiceTests
    {
        /// <summary>
        /// Ensures each PLC allocates its alarm-reference relation ID so independent HMI clients cannot collide on a
        /// shared fixed identifier.
        /// </summary>
        [Fact]
        public void CreateRequestsPlcAssignedAlarmReferenceRelationId()
        {
            var session = new CapturingProtocolSession();
            var service = new S7CommPlusAlarmSubscriptionService(session);

            service.Create(new uint[] { 1031 }, initialCreditLimit: 10, out _);

            var request = Assert.IsType<CreateObjectRequest>(session.Request);
            var alarmReference = request.RequestObject.GetObjects()
                .Single(child => child.ClassId == Ids.AlarmSubscriptionRef_Class_Rid);
            Assert.Equal((uint)Ids.GetNewRIDOnServer, alarmReference.RelationId);
        }

        /// <summary>
        /// Captures the outgoing create request without requiring a PLC response, allowing object-shape regressions to
        /// be tested independently of transport and authentication behavior.
        /// </summary>
        private sealed class CapturingProtocolSession : IS7CommPlusProtocolSession
        {
            /// <summary>
            /// Gets the request passed to <see cref="SendFunctionAndWait"/>.
            /// </summary>
            internal IS7pRequest Request { get; private set; } = null!;

            /// <summary>
            /// Gets or sets the simulated native protocol error.
            /// </summary>
            public int LastError { get; set; }

            /// <summary>
            /// Gets a nominal receive timeout; this test never waits for a response.
            /// </summary>
            public int ReadTimeout => 1;

            /// <summary>
            /// Gets a stable fake server-session identifier.
            /// </summary>
            public uint SessionId => 1;

            /// <summary>
            /// Gets a stable fake secondary session identifier used by create-object requests.
            /// </summary>
            public uint SessionId2 => 2;

            /// <summary>
            /// Gets an empty receive buffer because the simulated send fails before decoding.
            /// </summary>
            public MemoryStream ReceivedPdu { get; } = new MemoryStream();

            /// <summary>
            /// Rejects an unexpected fire-and-forget request.
            /// </summary>
            /// <param name="request">The unexpected request.</param>
            /// <returns>This method never returns.</returns>
            public int SendFunction(IS7pRequest request)
            {
                throw new System.NotSupportedException();
            }

            /// <summary>
            /// Captures the request and returns a transport error so the service does not require a serialized response.
            /// </summary>
            /// <param name="request">The request whose object graph is under test.</param>
            /// <returns>A nonzero simulated transport error.</returns>
            public int SendFunctionAndWait(IS7pRequest request)
            {
                Request = request;
                return 1;
            }

            /// <summary>
            /// Rejects an unexpected direct wait.
            /// </summary>
            /// <param name="timeoutMilliseconds">The unused timeout.</param>
            public void WaitForPdu(int timeoutMilliseconds)
            {
                throw new System.NotSupportedException();
            }

            /// <summary>
            /// Rejects an unexpected notification wait.
            /// </summary>
            /// <param name="subscriptionObjectId">The unused subscription identifier.</param>
            /// <param name="timeoutMilliseconds">The unused timeout.</param>
            /// <param name="notification">Receives no notification.</param>
            /// <returns>This method never returns.</returns>
            public int WaitForNotification(uint subscriptionObjectId, int timeoutMilliseconds, out Notification notification)
            {
                throw new System.NotSupportedException();
            }

            /// <summary>
            /// Rejects response validation because no response is generated by this test double.
            /// </summary>
            /// <param name="request">The unused request.</param>
            /// <param name="response">The unused response.</param>
            /// <returns>This method never returns.</returns>
            public int CheckResponse(IS7pRequest request, IS7pResponse response)
            {
                throw new System.NotSupportedException();
            }

            /// <summary>
            /// Rejects an unexpected object deletion.
            /// </summary>
            /// <param name="objectId">The unused object identifier.</param>
            /// <returns>This method never returns.</returns>
            public int DeleteObject(uint objectId)
            {
                throw new System.NotSupportedException();
            }

            /// <summary>
            /// Accepts the service's expected cleanup after the simulated transport failure.
            /// </summary>
            public void DisconnectTransport()
            {
            }

            /// <summary>
            /// Rejects unexpected session reset behavior.
            /// </summary>
            public void ClearSessionIds()
            {
                throw new System.NotSupportedException();
            }
        }
    }
}
